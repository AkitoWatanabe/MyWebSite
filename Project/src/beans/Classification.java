package beans;

public class Classification {
	private int classification_id;
	private String classification_id_name;


	public int getClassification_id() {
		return classification_id;
	}
	public void setClassification_id(int classification_id) {
		this.classification_id = classification_id;
	}
	public String getClassification_id_name() {
		return classification_id_name;
	}
	public void setClassification_id_name(String classification_id_name) {
		this.classification_id_name = classification_id_name;
	}
}
